#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char a;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -1 * n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	a = '0' + n % 10;
	write(fd, &a, 1);
}
